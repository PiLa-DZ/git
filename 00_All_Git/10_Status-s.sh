#
# --------------------------------------
# 2. Modified Files
# M (Red): ----> in Working Directory
# M (Green): --> in Staging Area
#
# --------------------------------------
# 3. Deleted Files
# D (Red):-----> in Working Directory
# D (Green):---> in Staging Area
