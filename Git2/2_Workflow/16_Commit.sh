# ===========================================================
# *** How Commit Works ***
# One Commit Create a Snapshot of your entire project
# - The Content --> The actual changes you made to the code.
# - The Author ---> Your name and email (Nabil).
# - The Timestamp > Exactly when the "box was sealed."
# - The Hash -----> A unique ID (e.g., a1b2c3d) that acts as a fingerprint.
# - The Message --> Your explanation of why you made the change.

# ===========================================================
# *** How to Commit (Terminal Style) ***
git commit -m "msg"  # The Quick Way
git commit -am "msg" # From Word Dir to Commit (For Tracked Files)
git commit           # The Detailed Way (Recommended)
